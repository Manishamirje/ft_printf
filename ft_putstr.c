#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		return (write(1, "(null)", 6));
	}
	while (*s != '\0')
	{
		ft_putchar((int)*s);
		i++;
		s++;
	}
	return (i);
}
